/* Type Casting */

#include <iostream>

using namespace std;

int main()
{
	
	double number = 100.0;
	       
	cout << "number           = " << number            << "\n";
	
	cout << "1 / 2 * number   = " << 1 / 2  * number   << "\n";
	
	cout << "number * (1 / 2) = " << number * (1 / 2)  << "\n";
	
	cout << "number * 1 / 2   = " << number * 1 / 2    << "\n";
	
	
	return 0; 
	
}